# basic operations

def add(x, y):
    print("Addition:", x + y)

add(2, 3)

def subtract(x, y):
    print("Subtraction:", x - y)

subtract(10, 3)

def multiply(x, y):
    print("Multiplication:", x * y)

multiply(2, 5)

def divide(x, y):
    print("Division:", x / y)

divide(10, 2)

# Modulo or "Mod" Operator (returns the remainder)

def mod(x, y):
    print("Modulo:", x % y)

mod(7, 4)